
def suma( a,b):
    """Funcion para suma de dos numeros"""
    resultado = a + b
    print(resultado)

def resta( a, b):
    """Funcion de resta de dos numeros"""
    resultado = a - b
    print(resultado)

def multiplicacion(a,b):
    """Funcion de multiplicacion de dos numeros"""
    resultado = a * b
    print(resultado)

def division(a,b):
    """Funcion de Division de dos numeros"""
    resultado = a / b
    print(resultado)